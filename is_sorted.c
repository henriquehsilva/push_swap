#include "push_swap.h"

int	is_sorted(t_stack *s)
{
	t_node	*tmp;

	if (!s)
		return (1);
	tmp = s->top;
	if (!tmp)
		return (1);
	while (tmp->next != NULL)
	{
		if (tmp->value > tmp->next->value)
			return (0);
		tmp = tmp->next;
	}
	return (1);
}
